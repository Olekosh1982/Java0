package lesson_02;

public class FirstClass {
    public static void main(String[] args) {

        int myFirstVariable;  // декларация (объявление) переменной
        myFirstVariable = 1; // инициализция перменной(присвоение пременной)
        int mySecondVariable = 2;
        System.out.println("My first variable " + myFirstVariable);
        System.out.println(mySecondVariable);

        byte byteVariable = 25;
        System.out.println(byteVariable);
        short shortVariable = 32330;
        System.out.println(shortVariable);
        long longVariable = 22_100_000_000L * 100_000_000;
        System.out.println(longVariable);
        double doudleVariable;
//        doudleVariable=1.50;
        doudleVariable = 1.5665;
        System.out.println(doudleVariable);
        doudleVariable = 1.5665;
        System.out.println(doudleVariable);

    }
}
